void	print_token(t_token *lst)
{
    while (lst != NULL)
    {
        if (lst->value)
            printf("value = %s / type = %d\n", lst->value, lst->type);
        else
            printf("value = (null) / type = %d\n", lst->type);
        lst = lst->next;
    }
}

bool	ft_isspace(char c)
{
	if (c == 32 || (c >= 7 && c <= 13))
		return (true);
	return (false);
}

char	*substrdup(const char *src, int start, int len)
{
	char	*str;
	int		i;

	str = malloc(len + 1);
	if (!str)
		return (NULL);
	i = 0;
	while (i < len && src[start + i])
	{
		str[i] = src[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
